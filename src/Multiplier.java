public class Multiplier {
    private Float multExperience;
    private Float multMoney;

    public Multiplier(Float multExperience,Float multMoney) {
        this.multExperience = multExperience;
        this.multMoney=multMoney;
    }

    public Float getMultExperience() {
        return multExperience;
    }

    public void setMultExperience(Float multExperience) {
        this.multExperience = multExperience;
    }

    public Float getMultMoney() {
        return multMoney;
    }

    public void setMultMoney(Float multMoney) {
        this.multMoney = multMoney;
    }
}
